<h1>Manual de Usuario</h1>
<p>
  Este proyecto integra un modelo de lenguaje de inteligencia artificial basado en la arquitectura LLaMA, especializado en comprender y generar texto natural. En nuestro caso, lo utilizamos para generar descripciones dinámicas y contextualizadas de las distintas regiones lunares seleccionadas por el usuario.
</p>

<p>
  La aplicación fue desarrollada utilizando un stack tecnológico diverso: <strong>HTML</strong>, <strong>CSS</strong> y <strong>JavaScript</strong> para la interfaz web; <strong>TypeScript</strong> para tipado seguro y mantenibilidad del código frontend; y <strong>Python</strong> en el backend para gestionar la comunicación con el modelo de IA y procesar los datos lunares.
</p>

<p>
  Con el objetivo de ofrecer una experiencia accesible e intuitiva para todo tipo de usuarios, diseñamos una interfaz amigable que incluye controles interactivos: dos botones (<strong>“+”</strong> y <strong>“–”</strong>) permiten ajustar dinámicamente el tamaño de la imagen lunar, facilitando la exploración de detalles tanto generales como específicos.
</p>

<p>
  Además, mediante el movimiento del ratón, es posible rotar y explorar el modelo 3D del satélite natural, brindando una experiencia inmersiva y visualmente enriquecedora. En tiempo real, la página muestra las <strong>coordenadas lunares</strong> (latitud y longitud) correspondientes al punto observado, junto con imágenes de alta resolución de esa región específica de la superficie lunar.
</p>
