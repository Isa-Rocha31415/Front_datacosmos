<h1>User Manual</h1>
<p>
  This project integrates an artificial intelligence language model based on the LLaMA architecture, specialized in understanding and generating natural language. In our case, it is used to produce dynamic, context-aware descriptions of the various lunar regions selected by the user.
</p>

<p>
  The application was developed using a diverse technology stack: <strong>HTML</strong>, <strong>CSS</strong>, and <strong>JavaScript</strong> for the web interface; <strong>TypeScript</strong> for type-safe and maintainable frontend code; and <strong>Python</strong> on the backend to manage communication with the AI model and process lunar data.
</p>

<p>
  To provide an accessible and intuitive experience for all types of users, we designed a user-friendly interface featuring interactive controls: two buttons (<strong>“+”</strong> and <strong>“–”</strong>) allow dynamic adjustment of the lunar image’s size, facilitating exploration of both general and specific details.
</p>

<p>
  Additionally, by moving the mouse, users can rotate and explore the 3D model of the natural satellite, delivering an immersive and visually enriched experience. In real time, the page displays the corresponding <strong>lunar coordinates</strong> (latitude and longitude) for the observed point, along with high-resolution images of that specific region of the lunar surface.
</p>
